#include "libft.h"

int			ft_nbdigit(int nbr)
{
	int		i;

	i = 1;
	if (nbr == -2147483648)
		return (10);
	if (nbr > 0)
	{
		while (nbr > 10)
		{
			i++;
			nbr /= 10;
		}
	}
	else
		ft_nbdigit(-nbr);
	return (i);
}
