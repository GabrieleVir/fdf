#include "libft.h"

void		ft_putlstr(const char *str, int nb_z)
{
	int		i;

	i = 0;
	while (nb_z > -1)
	{
		while (str[i])
			i++;
		write(1, str, i);
		nb_z--;
		if (nb_z > -1)
		{
			write(1, &str[i], 1);
			str += i;
			i = 0;
		}
	}
}
